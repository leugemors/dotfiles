set -eu

# ---------------------------------------------------------------------------
#  Main program
# ---------------------------------------------------------------------------

pkg=$(ls -d */ | sed 's/\/$//')

for i in $pkg; do
    echo -e "\nStowing $i . . ."
    stow -v -R "$i"
done

exit 0

